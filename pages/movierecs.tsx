import {
  useSession,
  useSupabaseClient,
  useUser,
} from "@supabase/auth-helpers-react";
import { useRouter } from "next/router";
import Head from "next/head";
import Footer from "../components/Footer/Footer";
import Header from "../components/Header/Header";
import RecommendedMovieCard from "../components/RecommendedMovieCard/RecommendedMovieCard";
import { useEffect } from "react";

export default function MovieRecs() {
  const session = useSession();
  const router = useRouter();

  useEffect(() => {
    if (!session) {
      router.push("/login");
    } else {
      router.push("/movierecs");
    }
  });

  return (
    <div className="bg-slate-900">
      <div className="flex flex-col container mx-auto my-0 p-3">
        <Head>
          <title>CineMate</title>
          <meta name="description" content="Generated by create next app" />
          <meta name="viewport" content="width=device-width, initial-scale=1" />
          <link rel="icon" href="/favicon.ico" />
        </Head>
        <Header />
      </div>

      <div className="bg-slate-700">
        <div className="flex flex-col min-h-screen container mx-auto my-0 p-3">
          <h2 className="text-amber-50 text-2xl py-5">
            Your Recommended Movies
          </h2>
          <div className="flex flex-wrap">
            {session ? <RecommendedMovieCard session={session} /> : null}
          </div>
        </div>
      </div>
      <Footer />
    </div>
  );
}
